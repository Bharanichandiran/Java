package day3;

public class Break {
    public static void main(String[] args) {
                for (int i = 1; i <= 10; i++) {
                    if (i == 5) {
                        System.out.println("Breaking at i = " + i);
                        break; // Exit the loop when i equals 5
                    }
                    System.out.println("i = " + i);
                }
                System.out.println("Loop exited.");
            }
        }
        
    

